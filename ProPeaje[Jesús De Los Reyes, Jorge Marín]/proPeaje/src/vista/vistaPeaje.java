    private void btnAgregarVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarVMouseClicked
        try{
            String placa=txtPlacaV.getText();
            String marca=txtMarca.getText();
            int mod=Integer.parseInt(cbModelo.getSelectedItem().toString());
            int dd=Integer.parseInt(cbDia1.getSelectedItem().toString());
            int mm=Integer.parseInt(cbMes1.getSelectedItem().toString());
            int yyyy=Integer.parseInt(cbYear1.getSelectedItem().toString());
            mm--;
            Calendar c=GregorianCalendar.getInstance();
            c.set(yyyy, mm, dd);
            
            Vehiculo v;
            
            if((jrAutomovil.isSelected())&&(!jrCamion.isSelected())){   
                v= new Vehiculo(placa, marca, mod, c);
                
            }else if((!jrAutomovil.isSelected())&&(jrCamion.isSelected())){
                int ne=Integer.parseInt(txtNumEjesV.getText());
                int cc=Integer.parseInt(txtCapCarga.getText());
                boolean s=false;
                if((chbSi.isSelected())&&(!chbNo.isSelected())){
                    s=true;
                }else if((!chbSi.isSelected())&&(chbNo.isSelected())){
                    s=false;
                }else{
                    throw new NumberFormatException();
                }
                
                v = new Camion(placa, marca, mod, c, ne, cc, s);
                
            }else{
                    throw new NumberFormatException();
            }
            
            
            if(ap.agregarVehiculo(v)){
                    JOptionPane.showMessageDialog(null, "Se ha agregado el Vehículo");
                }
                else{
                    JOptionPane.showMessageDialog(null, "No se pudo agregar el Vehículo, ya existe esta placa registrada");
                }
            
        reiniciarCampoAgVeh();
            
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Digite todos los campos");
        }
    }//GEN-LAST:event_btnAgregarVMouseClicked

